          <h3>Teachers, Students, Directors...</h3>
          <p class="top-obj">Violinists of all levels use etudes to polish their technical skills. Etudes are short passages of music that usually focus on one specific tequnique, such as trills, double stops, or string crossings. However, as many of you have probably noticed, etudes are sold in books for a single composer. Teachers wind up with shelves full of etude books, from which they might only assign a few exercises. We developed this site to give teachers a quick way to search for and download individual, public domain etudes. Don't know the name of the etude? No problem. You can filter by skill level and technique and browse the results from our database.</p>
